package jp.asamomiji.assembler;

public class Label {
    private int number;

    public Label(int number) {
        this.number = number;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public String toString() {
        return String.format("L%d", number);
    }
}
